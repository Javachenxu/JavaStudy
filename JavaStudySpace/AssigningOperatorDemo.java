package JavaStudySpace;
//赋值运算符
public class AssigningOperatorDemo {
    public static void main(String[] args) {
        int age;
        age = 10;
        int a = 9;
        int b = 3;
        System.out.println(a += b);
        System.out.println(a -= b);
        System.out.println(a *= b);
        System.out.println(a /= b);
        System.out.println(a %= b);
    }
}