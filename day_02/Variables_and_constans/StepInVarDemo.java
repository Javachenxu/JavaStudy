package JavaStudySpace.day_02.Variables_and_constans;
//引入变量
public class StepInVarDemo {
    public static void main(String[] args) {
        int a = 6;
        int b = 3;
        //需求：接到客户的一个需求，求两个操作数6和3的和，差，积与商。
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
    }
}