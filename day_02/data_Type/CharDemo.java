package JavaStudySpace.day_02.data_Type;

public class CharDemo {
    public static void main(String[] args) {
        //char的常量表示
        //char的变量表示
        System.out.println('@');
        System.out.println('7');
        System.out.println('A');
        char a = 65;
        System.out.println(a);
        char b = '\u0061';
        System.out.println(b);
        char c = '码';
        System.out.println(c);
    }
}