package JavaStudySpace.day_02.data_Type;
//整数类型（byte、short、long、int）
public class IntegerDemo {
    public static void main(String[] args) {
        //整数的常量表示22
        System.out.println(0b10110);//二进制
        System.out.println(026);// 八进制
        System.out.println(22);//十进制
        System.out.println(0X16);//十六进制
        System.out.println("------------------------");
        //整数的变量表示：数据类型 变量名 = 值；
        byte day = 1;
        System.out.println(day);
    }
}